package Calcularor;

public class Calculator {

    double add(double x, double y){
        double sum = x + y;
        return sum;
    }

    double substract(double x, double y){
        double sum = x - y;
        return sum;
    }

    double multiply(double x, double y){
        double sum = x * y;
        return sum;
    }

    double devide(double x, double y){
        double sum = x / y;
        return sum;
    }
}
